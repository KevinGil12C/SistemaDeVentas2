    private void btActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btActualizarActionPerformed
        int id = Integer.parseInt(labelID.getText());
        String des = txtDescripcion.getText();
        String format = "yyyy-MM-dd";
        Date date = dateFecha.getDatoFecha();
        SimpleDateFormat dateFormat = new SimpleDateFormat(format);
        float tot = Float.parseFloat(txtPrecio.getText());
        DecimalFormat decimalFormat = new DecimalFormat("#.00");
        String precioFormateado = decimalFormat.format(tot);
        float precioFinal = Float.parseFloat(precioFormateado);
        if (id != 0) {
            // Los datos son vÃ¡lidos, continÃºa con el proceso
            c = new Compras();
            c.setIdCompra(id);
            c.setDescripcion(des);
            c.setFecha(dateFormat.format(date));
            c.setTotal(precioFinal);
            String mensaje = cbo.actualizarCompra(c);
            if (!mensaje.equals("ACTUALIZADO CORRECTAMENTE")) {
                new rojerusan.RSNotifyFade("Â¡ERROR!", "Â¡Error al guardar al empleado!", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
            } else {
                new rojerusan.RSNotifyFade("Â¡SUCCESS!", "Â¡Guardado Correctamente!", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
                if (panelListener != null) {
                    panelListener.onPanelClosed();
                }
                dispose();
            }
        } else {
            new rojerusan.RSNotifyFade("Â¡WARNING!", "Â¡Llene todos los campos!", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
        }

    }//GEN-LAST:event_btActualizarActionPerformed

