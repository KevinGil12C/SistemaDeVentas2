    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int id;
        String nom, ape1, ape2, rfc, gen, cor, tel;

        if (labelID.getText() != null && !labelID.getText().isEmpty()
                && txtNombre.getText() != null && !txtNombre.getText().isEmpty()
                && txtAp.getText() != null && !txtAp.getText().isEmpty()
                && txtAm.getText() != null && !txtAm.getText().isEmpty()
                && txtRFC.getText() != null && !txtRFC.getText().isEmpty()
                && comboGenero.getSelectedItem() != null
                && txtCorreo.getText() != null && !txtCorreo.getText().isEmpty()
                && txtTel.getText() != null && !txtTel.getText().isEmpty()) {

            id = Integer.parseInt(labelID.getText());
            nom = txtNombre.getText();
            ape1 = txtAp.getText();
            ape2 = txtAm.getText();
            rfc = txtRFC.getText();
            gen = comboGenero.getSelectedItem().toString();
            cor = txtCorreo.getText();
            tel = txtTel.getText();

            Cliente c = new Cliente();
            c.setIdCliente(id);
            c.setNomCliente(nom);
            c.setApe1Cliente(ape1);
            c.setApe2Cliente(ape2);
            c.setRfcCliente(rfc);
            c.setGeneroCliente(gen);
            c.setCorreoCliente(cor);
            c.setTelCliente(tel);

            String mensaje = cbo.agregarCliente(c);

            if (!mensaje.equals("GUARDADO CORRECTAMENTE")) {
                new rojerusan.RSNotifyFade("¡ERROR!", "¡Error al guardar al cliente!", 3, RSNotifyFade.PositionNotify.TopLef.TopLef, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
            } else {
                new rojerusan.RSNotifyFade("¡SUCCESS!", "¡Guardado Correctamente!", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
                if (panelListener != null) {
                    panelListener.onPanelClosed();
                }
                dispose();
            }

        } else {
            new rojerusan.RSNotifyFade("¡WARNING!", "¡Llene todos los campos!", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

