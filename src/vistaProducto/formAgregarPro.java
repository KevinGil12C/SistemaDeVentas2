    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        int id = Integer.parseInt(labelID.getText());
        String nom = txtProducto.getText();
        String tam = comboTam.getSelectedItem() + "";
        String cat = comboCat.getSelectedItem() + "";
        float pre = Float.parseFloat(txtPrecio.getText());
        int cant = Integer.parseInt(txtCantidad.getText());

        if (id != 0 && nom != null && !nom.isEmpty() && tam != null && !tam.isEmpty()
                && cat != null && !cat.isEmpty() && pre >= 0 && cant >= 0) {
            // Los datos son válidos, continúa con el proceso
            Producto p = new Producto();
            p.setIdProducto(id);
            p.setNomProducto(nom);
            p.setTam(tam);

            // Formatear el precio con dos decimales
            DecimalFormat decimalFormat = new DecimalFormat("#.00");
            String precioFormateado = decimalFormat.format(pre);
            float precioFinal = Float.parseFloat(precioFormateado);

            p.setPrecio(precioFinal);
            p.setStock(cant);

            String mensaje = pbo.agregarProducto(p, cat);

            if (!mensaje.equals("GUARDADO CORRECTAMENTE")) {
                new rojerusan.RSNotifyFade("¡ERROR!", "¡Error al guardar el producto!", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
            } else {
                new rojerusan.RSNotifyFade("¡SUCCESS!", "¡Guardado Correctamente!", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
                if (panelListener != null) {
                    panelListener.onPanelClosed();
                }
                dispose();
            }
            idMax();
        } else {
            new rojerusan.RSNotifyFade("¡WARNING!", "¡Llene todos los campos correctamente!", 3, RSNotifyFade.PositionNotify.BottomRight, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
        }


    }//GEN-LAST:event_btnRegistrarActionPerformed

